function translateFAQ() {
    const currentLang = (window.safeStorage && window.safeStorage.getItem('userLang')) || 'pt';
    
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (faqTranslations[currentLang] && faqTranslations[currentLang][key]) {
            // Preserva HTML interno (como <strong>, <em>)
            const translation = faqTranslations[currentLang][key];
            if (element.innerHTML.includes('<strong>') || element.innerHTML.includes('<em>')) {
                element.innerHTML = translation;
            } else {
                element.textContent = translation;
            }
        }
    });
} 

// Traduz ao carregar
translateFAQ();

// Observa mudanças no idioma vindo de outras abas
window.addEventListener('storage', (e) => {
    if (e.key === 'userLang') {
        translateFAQ();
    }
});

// Observa mudança local via evento customizado disparado por safeStorage
window.addEventListener('odyssee-storage', (e) => {
    if (e.detail && e.detail.key === 'userLang') {
        translateFAQ();
    }
});
